    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            File temp = null;
            boolean bandera = false;

            if (jRadioButton1.isSelected()) {

                temp = linux.scriptCreator(jTextField2.getText(), jTextField1.getText());

                if (!jTextField1.getText().isEmpty()) {

                    bandera = true;
                }

            } else if (jRadioButton2.isSelected()) {

                temp = linux.scriptCreator(jTextField2.getText(), jComboBox1.getItemAt(jComboBox1.getSelectedIndex()).toString());
                bandera = true;

            }

            if (bandera) {

                Thread.sleep(1000); // Wait for the system to not go too fast.

                Runtime.getRuntime().exec("./.tempLixScrip.sh"); // run the script Linux in Bash

                Thread.sleep(1000);   
            }else {
            
                jTextField1.setBackground(Color.red);
            }

            temp.delete();

        } catch (IOException ex) {
            jLabel1.setText("ERROR!");
            ex.printStackTrace();
        } catch (InterruptedException ex) {
            jLabel1.setText("ERROR!");
            ex.printStackTrace();

        }

    }//GEN-LAST:event_jButton1ActionPerformed

